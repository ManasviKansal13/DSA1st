#include<bits/stdc++.h>
using namespace std;

main(){
    int a=2;
    int b=3;

    //and 
    cout<<(a&b)<<endl;
    //or
    cout<<(a|b)<<endl;
    //not
    cout<<~a<<endl;
    //xor
    cout<<(a^b)<<endl;
    //left shift
    cout<<(3<<2)<<endl;
    //right shift
    cout<<(5>>2)<<endl;
}